.DATA
Numbers: .WORD 12, 20, 30, 40, 50, 60, 70, 80, 90, 100
msg1: .asciz "ELEMENT FOUND"
msg2: .asciz "ELEMENT NOT FOUND"

.TEXT
MOV R1, #40
MOV R2, #0
MOV R3,#9
LDR R5,=Numbers
MOV R6,#4
L1:CMP R2,R3
BGT NOTFOUND
ADD R4,R2,R3
MOV R4,R4,LSR #1
MLA R7, R6, R4, R5
LDR R8, [R7] 
CMP R1, R8
BEQ FOUND
BMI LOWER
BGT UPPER
NOTFOUND:
MOV r0,#0
SWI 0X011
FOUND:
MOV r0,#1
SWI 0X011
UPPER:
ADD R2, R4,#1
B L1
LOWER:
SUB R3, R4,#1
B L1